// Logical operator

// && - And 

//  True     &&     True   -----  True
//  False    &&     True   -----  False
//  True     &&     False   ----- False
//  False    &&     False   ----- False



console.log(6==6 && 7 == 7) // True
console.log(6!=6 && 7 == 7) // False
console.log(6==6 && 7 != 7) // False
console.log(6!=6 && 7 != 7) // False


// || - OR 

//  True     ||     True   -----  True
//  False    ||     True   -----  True
//  True     ||     False   ----- True
//  False    ||     False   ----- False

console.log(6==6 || 7 == 7) // True
console.log(6!=6 || 7 == 7) // True
console.log(6==6 || 7 != 7) // True
console.log(6!=6 || 7 != 7) // False

// ! 
// True --  False 
// False -- True

